% p18 例3 抛物插值
x = [100, 121, 144];
y = [10, 11, 12];
xx = 115; % 插值点
l0 = ((xx - x(2)) * (xx - x(3))) / ((x(1) - x(2)) * (x(1) - x(3))); 
l1 = ((xx - x(1)) * (xx - x(3))) / ((x(2) - x(1)) * (x(2) - x(3)));
l2 = ((xx - x(1)) * (xx - x(2))) / ((x(3) - x(1)) * (x(3) - x(2)));
yy = l0 * y(1) + l1 * y(2) + l2 * y(3)