% 非线性最小二乘法
clear;

x = [1, 1.25, 1.5, 1.75, 2.0];
y = [5.10, 5.79, 6.53, 7.45, 8.46];   
y_ = log(y);  % 取对数后的线性函数值

A = [length(x), sum(x); sum(x), sum(x.*x)];  % 法方程系数矩阵
c = [sum(y_); sum(x.*y_)];   % 法方程右端向量

L = inv(A) * c;   % 一次多项式系数

a = exp(L(1));   % 数学模型参数
b = L(2);        % 数学模型参数

plot(x, y, 'o');    


